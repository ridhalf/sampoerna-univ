            <p class="decs">
            An “I” grade (Incomplete or Hold) may, at the discretion of the
            Faculty, be given in courses for a student who has a reasonable
            chance of successfully completing the course. The student who has
            not completed the required course work by the end of the term may be
            required to provide documentation for extenuating circumstances. The
            student should make arrangements to have the “I” changed to a final
            grade by the instructor (by the agreed upon date) during the next
            full term (summer terms are not considered in this time limit). If
            no change is initiated during the next full term, the “I”will
            automatically become an “F” on the student’s permanent record. If
            the course work is completed, resulting in a passing grade, the
            student's transcript will be amended and his/her final GPA
            re-calculated.
