            <p class="decs">
              All students, faculty and staff subscribe to the Emergency
              Notification System (ENS) and receive email alerts in the
              event of campus-wide emergencies or incidents.  These same
              notifications are also posted on the SU website and
              distributed via Instagram.  All students, faculty and staff
              are responsible to ensure that their contact information is
              up-to-date and accurate.  In addition, emergencies requiring
              the evacuation of SU facilities is also broadcast over a
              campus-wide public address system.
            </p>
          </li>
          <li class="item">
            <img
              src="assets/img/pages/safety-and-environment/list-procedures-for-investigating.webp"
