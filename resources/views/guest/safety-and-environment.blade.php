            />
            <p class="decs">
              All incidents on campus are followed up with an incident
              report, which allows Administrative Services to document the
              incident, evaluate the response and determine if further
              action is needed. Incident reports are filled out by faculty
              or staff.
            </p>
          </li>
          <li class="item">
            <img
              src="assets/img/pages/safety-and-environment/list-student-responsibilities.webp"
              alt=""
            />
            <p class="decs">
              Safety instruction is an integral part of SU academic
              programs, especially those involving laboratories. It is the
              student's responsibility to adhere to institutional policy and
              procedures relating to student conduct and campus health and
              safety. Students are responsible for personal safety and the
              safety of others in SU facilities and for immediately
              reporting any health, safety or security concerns to faculty
              or staff.
            </p>
          </li>
          <li class="item">
            <img
              src="assets/img/pages/safety-and-environment/list-FacultyStaff Responsibilities.webp"
              alt=""
            />
            <p class="decs">
              Faculty and staff are responsible for overseeing safety
              training for students and enforcement of safety in classrooms
              and lab areas. Faculty/staff lead safety training during
              course orientations and as safety concerns arise. Students
              must comply with all safety training requirements. In the
              event of an injury, students are not to be transported to a
              medical facility or home by the faculty or staff. They should
              alert Administrative Services to arrange for appropriate
              transportation.
            </p>
          </li>
          <li class="item">
            <img
              src="assets/img/pages/safety-and-environment/list-Campus Security-Officers.webp"
              alt=""
            />
            <p class="decs bold">
              Campus security officers are present on every floor of SU.
              They report to Administrative Services and have
              responsibilities on all parts of the campus. The major
              responsibility of campus security officers is to give
              attention to every situation which might involve the safety
              and welfare of faculty, staff, students and visitors. When
              there are infractions of local or national laws and/or college
              regulations, the campus security officers are authorized to
              take appropriate action. For more serious incidents that pose
              a direct threat to lives and property, local police will be
              notified to respond. In addition, campus security officers
              will refer faculty, staff and students to Administrative
              Services for internal action.
            </p>
          </li>
          <li class="item">
            <img
              src="assets/img/pages/safety-and-environment/list-Safety-Resources.webp"
              alt=""
            />
            <p class="decs">
              Academic program specific safety resources such as sharps
              disposal, material safety data sheets, biological pathogen
              disposal kits, eye protection, hand protection, hearing
              protection, eye wash and safety shower stations are located in
              the appropriate areas. The College is serviced by overhead
              sprinklers to assist with fire mitigation. Evacuation maps and
              first aid kits are also available on each floor. The campus is
              currently served by two health clinics staffed by professional
              medical personnel.
            </p>
          </li>
          <li class="item">
            <img
              src="assets/img/pages/safety-and-environment/list-Behavioral-Change.webp"
              alt=""
            />
            <p class="decs">
              A behavioral disturbance can be either verbal or physical.
              Everyone is to assist in making the campus a safe place by
              being alert to suspicious situations and promptly reporting
              them.
            </p>
            <p class="decs">
              Individuals who are a victim of or a witness to an on-campus
              offense should avoid placing themselves at greater risk. They
              should report any suspicious behavior or offense to a faculty
              or staff member as soon as possible and report the incident
              using the Incident Report Form. If you observe a criminal act,
              notify General Affairs.
            </p>
            <p class="decs">
              If you observe someone appearing to be threatening with a
              weapon, explosive, or other dangerous device that could
              potentially threaten lives, notify General Affairs (GA)
              immediately. GA will notify security to secure the situation
              and notify local police. If there is a direct exit away from
              the event, use it. Do not enter hallways and do not pull the
              fire alarms. If a direct exit is not immediately available,
              follow lockdown in place procedures. Move to a classroom or
              office, lock the doors, make sure the lights are turned off
              and stay clear of doors and windows. When security and/or
              police officials arrive, follow all instructions. Remain in
              lockdown until security or campus officials provides an all
              clear signal.
            </p>
          </li>
          <li class="item">
            <img
              src="assets/img/pages/safety-and-environment/list-Fire.webp"
              alt=""
            />
            <p class="decs">
              During orientation each semester, all faculty, staff and
              students are notified of fire extinguishers, fire hoses,
              emergency exits and manual alarm systems across campus and how
              to use them. Specific training is provided to Safety Captains
              who are on duty on each floor. Safety Captains receive
              specific training regarding the use of fire extinguishers
              and/or hoses, assessing a fire hazard, and notifying emergency
              officials
            </p>
            <p class="decs">
              The main building has an active fire control detection and
              suppression system. Signs of an active fire (smoke, gas odors,
              and/or increased room temperatures) automatically activates
              the fire alarm. In addition, there are manual station alarms
              located throughout the campus that can be activated by
              breaking glass and pulling the alarm
            </p>
            <p class="decs">
              If a minor fire appears controllable, Administrative Services
              should be immediately notified. The individual or local Safety
              Captain should then promptly direct the charge of the fire
              extinguisher or water from the fire hose toward the base of
              the flame
            </p>
            <p class="decs">
              If the fire appears uncontrollable and poses a threat to
              health and safety, General Affairs should be immediately
              notified to request that the building evacuation alarm be
              sounded (if the automatic system has not previously sounded). 
              All rooms should then be evacuated, closing all doors to
              confine the fire and reduce oxygen.  Doors must not be locked
              upon exit.
            </p>
            <p class="decs">
              Once the building evacuation alarm is sounded, the nearest
              Safety Captain will be mobilized to assist with the evacuation
              of their area by directing people to safety exits. Additional
              help will be provided to any handicapped individuals that
              require assistance. If a smoke event exists, the Safety
              Captain will remind everyone to stay as close to the floor as
              possible during evacuation. Evacuees will muster at three
              designated safety muster points located in outside areas clear
              of hazards.
            </p>
            <p class="decs">
              Individuals will not return to the buildings until an “all
              clear” has been given by Emergency Responders or College
              officials.
            </p>
            <ul class="notes">
              Notes:
              <li>
                If you become trapped in a building during a fire and a
                window is available, place an article of clothing (shirt,
                coat, etc.) outside the window as a signal for the rescue
                crews. If there is no window, stay near the floor where the
                air will be less toxic. Shout at regular intervals to alert
                emergency crews. Do not panic.
              </li>
              <li>
                After any evacuation, report to your designated campus
                assembly point. Stay there until an accurate headcount is
                taken.
              </li>
            </ul>
          </li>
          <li class="item">
            <img
              src="assets/img/pages/safety-and-environment/list-Earthquakes.webp"
              alt=""
            />
            <p class="decs">
              When an earthquake is detected, take cover under a desk, table
              or in a doorway; or stand close to the wall in a hallway. Stay
              away from windows or objects that could break or shatter. Wait
              approximately two minutes after tremors stop OR until the
              building evacuation order is given and then evacuate as per
              fire evacuation instructions.
            </p>
          </li>
          <li class="item">
            <img
              src="assets/img/pages/safety-and-environment/list-Medical-Emergencies.webp"
              alt=""
            />
            <p class="decs">
              Student illnesses or minor emergency medical care requirements
              should be reported to the nearest faculty or staff member.
              These problems will be resolved by utilizing first aid kits or
              a referral to one of the health clinics. No medical care will
              be administered by faculty or staff (other than trained
              medical professionals assigned to the clinics). If a serious
              or life threatening medical emergency exists, immediately call
              112.
            </p>
          </li>
          <li class="item">
            <img
              src="assets/img/pages/safety-and-environment/list-Weapon-and-Firearms.webp"
              alt=""
            />
            <p class="decs">
              Fireworks, firearms, ammunition and other weapons or materials
              which endanger health and safety are strictly prohibited. The
              possession or use of these items at SU will result in
              disciplinary action. Such incidents that also violate local
              and/or national laws may also result in prosecution.
            </p>
          </li>
          <li class="item">
            <img
              src="assets/img/pages/safety-and-environment/list-Environmental-Hazard.webp"
              alt=""
            />
            <p class="decs">
              Blood Spills <br />
              If you see blood on the floor or other surface, notify a
              faculty or staff member. Do not attempt to clean up the blood
              yourself or allow others to do so. If you are exposed to
              another person's blood or other bodily fluids: <br />
              For a skin exposure or blood that comes into contact with an
              open wound, wash the area thoroughly with soap and water.
              <span class="bloods">
                For a blood splash to the eyes, nose or mouth, rinse
                copiously with distilled water. Report any blood exposure to
                faculty or staff.
              </span>
            </p>
            <p class="decs">
              Other Hazardous Materials <br />
              Other hazardous materials or substances can be in the form of
              chemical spills, gas leaks or other substances. If you see or
              smell a substance that you feel may be hazardous, stay away
              from the substance and direct others away from the immediate
              area. Do not touch or attempt to clean up an unidentified
              substance. Notify faculty or staff of the incident for further
              action as needed.
            </p>
            <p class="decs">
              Note: Individuals exposed to any hazardous materials in the
              laboratories of SU should immediately notify faculty or staff
              on duty. If needed, they will be directed to use the Eyewash
              and Safety Shower located in the lab area.
            </p>
          </li>
