      <h3>Safety, Health and Environment Plan</h3>
      <p class="decs">
        Sampoerna University (SU) complies with all relevant institutional,
        industrial and governmental regulations and policies regarding
        health, safety and security procedures. This plan is designed to
        promote a safer, more secure and healthful learning and work
        environment for the College community and its guests. This plan
        outlines the protocol for the management of health and safety of the
        SU community in cases of accidents, fire, safety, emergency and
        evacuation, security and safety on campus.
      </p>
      <p class="decs">
        The Safety, Health and Environment Plan (SHEP) is reviewed annually
        by an ad hoc committee appointed by the President of SU. This
        committee provides recommendations for updates and revisions; other
        updates and revisions are made whenever changes are made by relevant
        governmental or regulatory bodies.
      </p>
